# Criar um programa que calcule a área de um retângulo.
# area_rec = base * altura
# A altura e a base devem ser inseridas via teclado
# base: 15
# altura: 3

# Entrada - Input
base = float(input("Digite o valor da base: "))
altura = float(input("Digite o valor da altura: "))

# Processamento
area = base * altura

# Saída - Output(print)
print("A área do retângulo é: ", area)
