# Entrada - Input
num1 = float(input("Digite o 1o número: "))
num2 = float(input("Digite o 2o número: "))

# Processamento
soma = num1 + num2
sub = num1 - num2
div = num1 / num2
mult = num1 * num2

# Saída - Output(print)
print(num1, "+", num2,"=", soma)
print(num1, "-", num2,"=", sub)
print(num1, "/", num2,"=", div)
print(num1, "*", num2,"=", mult)
